export default function getTypeFunction(elementType) {
  switch (elementType) {
    case "fire":
      return "#ed7809";

    case "normal":
      return "#bab1a8";

    case "fighting":
      return "#bf9c7a";

    case "flying":
      return "#53c2cf";

    case "poison":
      return "#7d33cc";

    case "ground":
      return "#d1cfb0";

    case "rock":
      return "#dba77b";

    case "bug":
      return "#cbe670";

    case "ghost":
      return "#727596";

    case "steel":
      return "#c1d8db";

    case "water":
      return "#80a2d1";

    case "grass":
      return "#75e679";

    case "electric":
      return "#e1f296";

    case "psychic":
      return "#cd74d4";

    case "ice":
      return "#7ed3e0";

    case "dragon":
      return "#9b4ec2";

    case "dark":
      return "#5b1b7a";

    case "fairy":
      return "#d952de";
  }
}
